<h1>Tentang TeLL - Trisakti E-Learning</h1></td>    </tr>
                <td height="1193">
              <p class="tell">Perkembangan ilmu dan teknologi di era globalisasi
                dalam berbagai bentuk telah mengubah wajah kehidupan di seluruh 
                dunia. Dalam era globalisasi, batas-batas antar wilayah negara 
                menjadi hilang (borderless) sebagai akibat dari perkembangan teknologi 
                informasi dan komunikasi tersebut. Hal ini disebabkan karena teknologi 
                tersebut menjadikan individu dan institusi saling berkaitan (interconnected) 
                dalam suatu jejaring dunia (web based communication).</p>
              <p class="tell">Dampak dari perkembangan teknologi informasi dan 
                komunikasi ini juga melanda dunia pendidikan, sehingga telah menggeser 
                paradigma pendidikan yang berorientasi pada kelas (traditional-classroom 
                oriented) menjadi virtual classroom yang berorientasi pada mahasiswa 
                (student oriented). Universitas Trisakti sebagai universitas swasta 
                terkemuka telah menentukan langkah strategis melalui penerapan 
                pola pendidikan berbasis elektronik (cyber campus) dengan menyelenggarakan 
                Program Pembelajaran Elektronik dan Jarak Jauh atau dikenal pula 
                sebagai Trisakti E-Learning (TeLL).<br>
                Trisakti E-Learning (TeLL), diharapkan selalu memenuhi kebutuhan 
                para professional dalam peningkatan kemampuan untuk terus bersaing 
                meskipun berada dalam keterbatasan waktu dan jarak, yang seringkali 
                menjadi salah satu handicap bagi seseorang untuk belajar.<br>
              </p>
              <p class="tell">Trisakti E-Learning (Tell), telah menyelenggarakan 
                program pembelajaran elektronik dan jarak jauh (e-learning), melalui 
                penerapan teknologi sehingga proses pembelajaran di Universitas 
                Trisakti dapat dilakukan kapan saja dan di mana saja. Untuk itu 
                Universitas Trisakti telah bekerjasama dengan lembaga yang memiliki 
                pengalaman yang matang dan jaringan kerja yang luas dalam program 
                pembelajaran jarak jauh di wilayah Asia Tenggara (SEAMOLEC - SEAMEO 
                Regional Open Learning Center). Kerjasama ini dilaksanakan dalam 
                bentuk penggunaan platform awal Trisakti e-learning yaitu Software 
                WebCT. Namun, setelah hampir 10 tahun menggunakan platform WebCT, 
                Trisakti e-Learning berhasil membangun platform sendiri yang dinamakan 
                WebTell. Trisakti e-Learning juga didukung kuat oleh para stakeholder 
                khususnya dari pimpinan Universitas Trisakti untuk menggunakan 
                media pembelajaran online dalam rangka mengembangkan dan memperluas 
                lingkungan belajar mengajar dengan menggunakan keunggulan media 
                online.<br>
              </p>
              <p class="tell">Trisakti e-Learning (TeLL) memberikan fasilitas 
                belajar mengajar secara virtual dengan memanfaatkan perangkat 
                internet dengan mengombinasikan pola pembelajaran on-line sebanyak 
                70 persen dan tatap muka di kelas (face to face) sebanyak 30 persen. 
                Dengan komposisi pembelajaran seperti itu sangat memungkinkan 
                bagi peserta didik yang memiliki keterbatasan waktu atau secara 
                geografis berjauhan dengan lokasi kampus Universitas Trisakti 
                untuk tetap dapat mengikuti perkuliahan. Saat ini, TeLL baru menyelenggarakan 
                program pendidikan S-2 program studi Magister Manajemen (MM) sebagai 
                alternatif pembelajaran MM reguler yang mewajibkan mahasiswa untuk 
                datang ke kampus. <br>
              </p>
              <p class="tell"><font size="5">Logo Tell dan Maknanya </font></p>
              
            <p class="tell"><img src="logo.png" width="303" height="157"></p>
            <p class="tell">&nbsp;</p>
          <p>1. Warna Biru pada Elips Mengandung arti Pengetahuan, Kekuatan, 
              Integritas dan Keseriusan yang tiada putus dari pengelola program 
              dalam melaksanakan proses pembelajaran yang berkualitas.</p>
            <p>2. Warna Putih pada Tulisan TeLL Mengandung makna Sederhana/Simple 
              dalam arti Trisakti e-learning menyederhanakan produk media pembelajaran 
              yang berteknologi tinggi sehingga mudah digunakan oleh setiap orang.<br>
            </p>
            <p>3. Bentuk Elips Melambangkan Ilmu yang Tidak Terbatas dan selalu 
              berkembang ke arah kemajuan bagi kesejahteraan umat manusia dan 
              kelestarian lingkungan.<br>
            </p>
            <p>4. Background Lambang Trisakti bergaris Melambangkan bahwa Universitas 
              Trisakti melalui Trisakti e-learning menawarkan pembelajaran yang 
              multi channel, yang menggunakan semua media pembelajaran bagi suksesnya 
              proses pembelajaran itu sendiri.<br>
            </p>
            <p>5. Tulisan TeLL Merupakan singkatan dari Trisakti e-learning yang 
              sekaligus mengandung makna &quot;Membagi Ilmu Kepada Semua Orang 
              (Share The Knowledge to Human Beings) &quot;.<br>
            </p>
