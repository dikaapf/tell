          <p><strong>Bagaimana belajar secara virtual?</strong><br />
            <br />
                Sebagai sistem pembelajaran virtual berbasis web, maka keraguan 
                yang sering menjadi pertanyaan peserta didik adalah Apakah saya 
                dapat mengikuti sistem pembelajaran ini?. Jawabannya adalah pasti 
                bisa. Di era digital ini sangat sulit menemukan professional yang 
                <em>computer illiterate</em>. Pada umumnya professional sudah 
                terbiasa menggunakan media komunikasi e-mail dan chat. Nah, jika 
                anda bisa menggunakan kedua cara komunikasi ini, maka anda dapat 
                mengikuti sistem pembelajaran virtual yang ditawarkan oleh TeLL.<br />
            <br />
                TeLL menggunakan platform pembelajaran Online Tell yang memang 
                secara khusus didisain untuk mereplikasi kelas dalam artian fisik 
                menjadi kelas virtual. Sistem ini dapat diakses oleh browser yang 
                umum digunakan seperti : internet explorer, firefox,netscape dan 
                opera. Melalui browser ini anda login ke Online Tell melalui <a href="http://www.tell,trisakti.ac.id">www.tell,trisakti.ac.id</a> 
                . <br />
                Begitu anda login, anda dapat memilih menu belajar anda. Secara 
                umum menu ini dibagi menjadi <em>synchrounous</em> dan <em>asynchronous</em>. 
                Pada menu <em>synchronous</em> mahasiswa dan dosen hadir pada 
                waktu yang sama sehingga dapat terjadi komunikasi dua arah. Termasuk 
                kedalam menu yang <em>synchronous</em>  adalah <em>chat </em>dan 
                <em>whiteboard.</em>  Chat adalah media komunikasi berbasis teks 
                sedangkan pada <em>whiteboard</em> , selain berkomunikasi melalui 
                teks, dosen dan mahasiswa dapat berinteraksi melalui gambar.  
                Media ini sangat relevan jika materi yang dibahas membutuhkan 
                tampilan visual. Pada menu <em>asynchronous</em> , dosen dan mahasiswa 
                tidak hadir pada saat yang sama, sehingga pesan yang disampaikan 
                tidak dapat direspons pada waktu yang sama. Termasuk kedalam menu 
                <em>asynchronous </em> adalah <em>email</em> dan <em>discussion</em>.<br />
            <br />
            Metoda mana yang akan  dipakai tentunya tergantung dari kesepakatan dosen dan mahasiswa, jadi jangan  lupa untuk selalu berkomunikasi dengan dosen yang bersangkutan. <br />
            <br />
                Selain menu diatas juga tersedia menu lain yang mendukung proses 
                pembelajaran, diantaranya adalah: <em>syllabus </em>(berisikan 
                informasi tentang mata kuliah yang bersangkutan), <em>calendar  
                </em>(jadwal atau waktu-waktu penting yang harus diperhatikan), 
                <em>content modules</em> (bahan ajar atau supplement yang disiapkan 
                oleh dosen). Silahkan anda masuk ke halaman-halaman tersebut dengan 
                jalan mengklik <em>hyperlink</em>-nya. Selamat belajar.</p>
          <p><strong>Digital  Library</strong><br />
            <br />
                Kesulitan mencari referensi untuk tugas mata kuliah? Tidak lagi.<br />
                Universitas Trisakti telah menyiapkan digital libabry yang juga 
                dapat diakses oleh mahasiswa TeLL. Silahkan anda klik <u>perpustakaan 
                online</u> (<a href="http://www.library.trisakti.ac.id">www.library.trisakti.ac.id</a>) 
                yang hyperlink nya terdapat pada web site ini. Anda dapat memilih 
                digital library yang tersedia, silahkan klik salah satunya. Jangan 
                lupa untuk mengecek password nya di halaman muka Online Tell , 
                silahkan mencoba.</p>
